using System;

namespace WB.UI.WebTester.Services.Implementation
{
    public interface IImportQuestionnaireAndCreateInterviewService
    {
        /// <summary>
        /// Starts the background import of the questionnaire and creation of the interview aggregate.
        /// </summary>
        /// <param name="questionnaireId">
        /// The questionnaire's Designer ID — passed through to <c>IQuestionnaireImportService</c>
        /// for all Designer API calls. Not used as a storage key.
        /// </param>
        /// <param name="interviewId">
        /// Unique per-run Guid generated by <c>WebTesterController.Run</c>. Used as the interview
        /// aggregate ID and as the key for all local stores (JWT, user-context, appdomains).
        /// </param>
        /// <param name="originalInterviewId">Optional: existing interview to replay data from.</param>
        /// <param name="scenarioId">Optional: scenario to apply after import.</param>
        /// <returns>The <paramref name="interviewId"/> that was passed in.</returns>
        Guid StartImportQuestionnaireAndCreateInterview(
            Guid questionnaireId,
            Guid interviewId,
            Guid? originalInterviewId,
            int? scenarioId);

        CreationResult? GetStatus(Guid interviewId);
        CreationResult? RemoveStatus(Guid interviewId);
    }
}
